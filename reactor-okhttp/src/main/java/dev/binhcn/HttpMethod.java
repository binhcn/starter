package dev.binhcn;

/**
 * The HTTP Request methods.
 */
public enum HttpMethod {
    /**
     * The HTTP GET method.
     */
    GET,

    /**
     * The HTTP PUT method.
     */
    PUT,

    /**
     * The HTTP POST method.
     */
    POST,

    /**
     * The HTTP PATCH method.
     */
    PATCH,

    /**
     * The HTTP DELETE method.
     */
    DELETE,

    /**
     * The HTTP HEAD method.
     */
    HEAD,
}